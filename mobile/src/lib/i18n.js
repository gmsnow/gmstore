const I18nContext = createContext(null);

export function I18nProvider({ children }) {
  const [locale, setLocaleState] = useState("ar");

  useEffect(() => {
    AsyncStorage.getItem("locale").then((l) => { if (l) setLocaleState(l); });
  }, []);

  const dict = locale === "en" ? en : ar;

  function t(key, vars = {}) {
    let val = dict[key] || ar[key] || key;
    for (const [k, v] of Object.entries(vars)) val = val.replace(`{${k}}`, v);
    return val;
  }

  async function setLocale(l) {
    setLocaleState(l);
    await AsyncStorage.setItem("locale", l);
  }

  return (
    <I18nContext.Provider value={{ locale, direction: locale === "ar" ? "rtl" : "ltr", t, setLocale }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be inside I18nProvider");
  return ctx;
}
